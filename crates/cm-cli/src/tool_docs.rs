fn append_tool_summary_table(out: &mut String, tools: &[ToolContract], include_examples: bool) {
    out.push_str("## MCP Tools\n\n");
    if include_examples {
        out.push_str("| Tool | Purpose | Example |\n");
        out.push_str("|------|---------|---------|\n");
    } else {
        out.push_str("| Tool | Purpose |\n");
        out.push_str("|------|---------|\n");
    }

    for tool in tools {
        if include_examples {
            let example = tool
                .examples
                .first()
                .map(|example| format!("`{}`", example.invocation))
                .unwrap_or_default();
            let _ = writeln!(
                out,
                "| `{}` | {} | {} |",
                tool.name, tool.descriptions.short, example
            );
        } else {
            let _ = writeln!(out, "| `{}` | {} |", tool.name, tool.descriptions.short);
        }
    }
    out.push('\n');
}

