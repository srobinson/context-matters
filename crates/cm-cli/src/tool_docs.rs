fn append_heading(out: &mut String, level: usize, title: &str) {
    out.push_str(&"#".repeat(level));
    out.push(' ');
    out.push_str(title);
    out.push_str("\n\n");
}

fn scope_input_label(tool: &ToolContract) -> String {
    let Some(param) = tool.param("scope") else {
        return "No scope request parameter".to_string();
    };
    let requirement = if param.required {
        "Required"
    } else {
        "Optional"
    };
    match param.shape {
        ParamShape::Scope(ScopeInputShape::Singular) => {
            format!("{requirement} singular scope selector")
        }
        ParamShape::Scope(ScopeInputShape::Broad) => {
            format!("{requirement} singular or broad scope selector")
        }
        _ => format!("{requirement} custom scope parameter"),
    }
}

fn scope_fragment_label(fragment: ScopeFragment) -> &'static str {
    match fragment {
        ScopeFragment::ExactPathString => "canonical path string",
        ScopeFragment::Path => "path",
        ScopeFragment::CwdInferred => "cwd_inferred",
        ScopeFragment::Project => "project",
        ScopeFragment::Repo => "repo",
        ScopeFragment::Session => "session",
        ScopeFragment::Descendants => "descendants, subtree alias accepted",
        ScopeFragment::Set => "set",
        ScopeFragment::All => "all",
    }
}

fn is_broad_only(fragment: ScopeFragment) -> bool {
    matches!(
        fragment,
        ScopeFragment::Descendants | ScopeFragment::Set | ScopeFragment::All
    )
}

fn singular_scope_tool_names(tools: &[ToolContract]) -> Vec<&str> {
    tools
        .iter()
        .filter(|tool| {
            matches!(
                tool.param("scope").map(|param| &param.shape),
                Some(ParamShape::Scope(ScopeInputShape::Singular))
            )
        })
        .map(|tool| tool.name.as_str())
        .collect()
}

fn broad_scope_tool_names(tools: &[ToolContract]) -> Vec<&str> {
    tools
        .iter()
        .filter(|tool| {
            matches!(
                tool.param("scope").map(|param| &param.shape),
                Some(ParamShape::Scope(ScopeInputShape::Broad))
            )
        })
        .map(|tool| tool.name.as_str())
        .collect()
}

fn tools_with_names<'a>(tools: &'a [ToolContract], names: &[&str]) -> Vec<&'a str> {
    tools
        .iter()
        .filter(|tool| names.contains(&tool.name.as_str()))
        .map(|tool| tool.name.as_str())
        .collect()
}

fn inline_tool_list(tools: &[&str]) -> String {
    tools
        .iter()
        .map(|tool| format!("`{tool}`"))
        .collect::<Vec<_>>()
        .join(", ")
}

fn param_type_label(param: &ToolParamContract) -> String {
    if let Some(ev) = &param.enum_values {
        return format!("enum: {}", ev.join(" \\| "));
    }

    match &param.shape {
        ParamShape::Scalar(kind) => kind.clone(),
        ParamShape::Array { items } => match items {
            ArrayItems::Scalar(kind) => format!("array<{kind}>"),
            ArrayItems::ExchangeObject => "array<object>".to_string(),
        },
        ParamShape::Scope(_) | ParamShape::CustomSchema(_) => "string | object".to_string(),
    }
}
