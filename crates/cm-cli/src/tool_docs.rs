fn append_scope_model(out: &mut String, tools: &[ToolContract], heading_level: usize) {
    append_heading(out, heading_level, "Scope Model");
    out.push_str(
        "Scopes form a hierarchy. Context at broader scopes is visible at narrower scopes.\n\n",
    );
    out.push_str("```text\n");
    out.push_str("global                                           cross-project knowledge\n");
    out.push_str("global/project:helioy                           project-level decisions\n");
    out.push_str("global/project:helioy/repo:context-matters      codebase-specific facts\n");
    out.push_str("global/project:helioy/repo:context-matters/session:abc   task context\n");
    out.push_str("```\n\n");
    out.push_str("Public requests select scope through the `scope` field. The canonical scope path string returned by read tools can be passed directly to write tools.\n\n");
    append_scope_fragment_table(out, "Singular Selectors", ScopeInputShape::Singular, true);
    append_scope_fragment_table(out, "Broad Selectors", ScopeInputShape::Broad, false);
    append_scope_boundary_table(out, tools);
    out.push_str("For cwd based browse resolution, call `cx_browse(scope: {\"kind\":\"cwd_inferred\",\"cwd\":\"/path/to/repo\"})`.\n");
    out.push_str("`cwd_inferred` resolves linked git worktrees to the source repository identity. Persisted entries and export rows include `scope_path`. Read projections use `scope` for compact row output.\n\n");
}

fn append_scope_fragment_table(
    out: &mut String,
    title: &str,
    shape: ScopeInputShape,
    include_all: bool,
) {
    let fragments = shape
        .fragments()
        .iter()
        .copied()
        .filter(|fragment| include_all || is_broad_only(*fragment));
    out.push_str(&format!("### {title}\n\n"));
    out.push_str("| Selector | Request example |\n");
    out.push_str("|----------|-----------------|\n");
    for fragment in fragments {
        let _ = writeln!(
            out,
            "| {} | `{}` |",
            scope_fragment_label(fragment),
            fragment.request_example()
        );
    }
    out.push('\n');
}

fn append_scope_boundary_table(out: &mut String, tools: &[ToolContract]) {
    out.push_str("### Tool Scope Boundaries\n\n");
    out.push_str("| Tool | Scope request |\n");
    out.push_str("|------|---------------|\n");
    for tool in tools.iter().filter(|tool| tool.param("scope").is_some()) {
        let _ = writeln!(out, "| `{}` | {} |", tool.name, scope_input_label(tool));
    }
    out.push('\n');
}

fn append_two_phase_retrieval(out: &mut String, tools: &[ToolContract], heading_level: usize) {
    let snippet_tools = tools_with_names(tools, &["cx_recall", "cx_search", "cx_browse"]);
    let get_tool = tools
        .iter()
        .find(|tool| tool.name == "cx_get")
        .map(|tool| tool.name.as_str())
        .unwrap_or("cx_get");

    append_heading(out, heading_level, "Two-Phase Retrieval");
    let _ = writeln!(
        out,
        "{} return metadata plus snippets. Use `{get_tool}` with returned IDs to fetch full body content.",
        inline_tool_list(&snippet_tools)
    );
    out.push_str("This keeps initial responses compact while allowing selective deep reads.\n\n");
}

fn append_parameter_reference(out: &mut String, tools: &[ToolContract]) {
    out.push_str("\n## Parameter Reference\n\n");
    out.push_str("> Auto-generated from tools.toml.\n\n");

    for tool in tools {
        let _ = writeln!(out, "### `{}`\n", tool.name);
        let _ = writeln!(out, "{}\n", tool.descriptions.mcp);

        if !tool.params.is_empty() {
            out.push_str("| Parameter | Type | Required | Description |\n");
            out.push_str("|-----------|------|----------|-------------|\n");
            for param in &tool.params {
                append_parameter_row(out, param);
            }
            out.push('\n');
        }
    }
}

fn append_parameter_row(out: &mut String, param: &ToolParamContract) {
    let req = if param.required { "yes" } else { "no" };
    let type_str = param_type_label(param);
    let desc = if param.mcp_description.len() > 120 {
        format!("{}...", &param.mcp_description[..117])
    } else {
        param.mcp_description.clone()
    };
    let _ = writeln!(
        out,
        "| `{}` | {} | {} | {} |",
        param.name, type_str, req, desc
    );
}

